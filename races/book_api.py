def clean_text(text: str) -> str:

    text = re.sub(r'\r', ' ', text)

    text = re.sub(r'\n+', ' ', text)

    text = re.sub(r'\s+', ' ', text).strip()

    text = re.sub(r'\*\*\*.*?\*\*\*', ' ', text)

    text = re.sub(r'[^А-Яа-яЁё0-9\s.,!?;:—\-«»()]+', ' ', text)

    return re.sub(r'\s+', ' ', text).strip()

def pick_fragment(raw: str, min_len: int = 850, max_len: int = 1300) -> str:

    raw = clean_text(raw)

    if len(raw) <= max_len:

        return raw

    for _ in range(30):

        start = random.randint(0, max(0, len(raw) - max_len - 1))

        fragment = raw[start:start + max_len]

        dot = fragment.find('. ')

        if dot > 0:

            fragment = fragment[dot + 2:]

        last_dot = max(fragment.rfind('. '), fragment.rfind('! '), fragment.rfind('? '))

        if last_dot > min_len:

            fragment = fragment[:last_dot + 1]

        fragment = fragment.strip()

        if len(fragment) >= min_len and re.search(r'[А-Яа-яЁё]', fragment):

            return fragment

    return raw[:max_len].strip()

